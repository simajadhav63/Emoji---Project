    // Event listener for search input
    searchInput.addEventListener("input", function() {
        const searchTerm = searchInput.value.toLowerCase();
        
        // Filter emojis based on the search term
        const filteredEmojis = emojis.filter(emoji =>
            emoji.emoji.toLowerCase().includes(searchTerm) ||
            emoji.emotion.toLowerCase().includes(searchTerm) ||
            emoji.description.toLowerCase().includes(searchTerm)
        );
        
        // Sort filtered emojis by description matching first
        filteredEmojis.sort((a, b) => {
            const aIndex = a.description.toLowerCase().indexOf(searchTerm);
            const bIndex = b.description.toLowerCase().indexOf(searchTerm);
            return aIndex - bIndex;
        });

        // Display filtered emojis
        displayEmojis(filteredEmojis);
    });
